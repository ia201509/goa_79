my_list = ["a", "b", "c", "d", "e", "f", "g", "h", "i", "j"]

print("სია:", my_list)

num = int(input("შეიყვანე რიცხვი: "))

if num > 5:
    print("ამ ინდექსზე მდგომი ელემენტია:", my_list[num])
else:
    print("შეყვანილი რიცხვი 5-ზე მეტი არ არის")