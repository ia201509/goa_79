# 8) მომხარებელს შემოატანინე ერთი სტრინგი და შემდგომ ერთი ცალი ასო. იპოვე პირველ სტრინგში ამ მეორე ასოს ინდექსი.


text = input("შეიყვანე სტრინგი: ")
char = input("შეიყვანე ერთი ასო: ")
index = text.find(char)
if index != -1:
    print("პირველი ინდექსი:", index)
else:
    print("ასო სტრინგში ვერ მოიძებნა")